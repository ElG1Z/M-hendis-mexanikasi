# Assignment
a = 10
b = 5

# Addition assignment
a += b
print(a)  # Output: 15

# Subtraction assignment
a -= b
print(a)  # Output: 10

# Multiplication assignment
a *= b
print(a)  # Output: 50

# Division assignment
a /= b
print(a)  # Output: 10.0

# Modulus assignment
a %= b
print(a)  # Output: 0

# Exponentiation assignment
a **= b
print(a)  # Output: 0